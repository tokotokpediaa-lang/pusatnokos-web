export async function GET() {
  try {
    const res = await fetch(
      `${SA_BASE}?action=getCountries&api_key=${SA_KEY}`,
      { next: { revalidate: 3600 } }
    );
    if (!res.ok) throw new Error(`SA error: ${res.status}`);

    const raw: Array<{ id: number; eng: string; rus: string; visible: number }> = await res.json();

    const countries = raw
      .filter(c => c.visible === 1)
      .map(c => ({
        id:   String(c.id),
        name: c.eng,
        flag: COUNTRY_META[c.id]?.flag ?? 'рџЊђ',
        code: COUNTRY_META[c.id]?.code ?? '',
      }))
      .sort((a, b) => {
        const priority: Record<string, number> = {
          '6': 1,   // Indonesia
          '0': 2,   // Russia
          '22': 3,  // India
          '187': 4, // USA
          '52': 5,  // Thailand
          '10': 6,  // Vietnam
          '7': 7,   // Malaysia
          '4': 8,   // Philippines
        };
        const pa = priority[a.id] ?? 99;
        const pb = priority[b.id] ?? 99;
        if (pa !== pb) return pa - pb;
        return a.name.localeCompare(b.name);
      });

    return NextResponse.json({ provider: 'smsactivate', countries });
  } catch (err: any) {
    return NextResponse.json({ error: err.message }, { status: 500 });
  }
}